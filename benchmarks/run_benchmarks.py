#!/usr/bin/env python3
"""
Reproducible benchmark runner for PrivySHA.

Usage:
    python benchmarks/run_benchmarks.py
    python benchmarks/run_benchmarks.py --save
    python benchmarks/run_benchmarks.py --config balanced --output-dir benchmarks/output
"""

import argparse
import json
import os
import sys
from datetime import datetime
from pathlib import Path

# Allow running from repo root without install
ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(ROOT / "src"))

os.environ.setdefault("PRIVYSHA_DISABLE_ML", "1")

from privysha.core.benchmark import BenchmarkHarness  # noqa: E402
from privysha.core.policy_config import PolicyConfig  # noqa: E402


def main() -> int:
    parser = argparse.ArgumentParser(description="Run PrivySHA benchmarks")
    parser.add_argument(
        "--output-dir",
        "-o",
        default=str(ROOT / "benchmarks" / "output"),
        help="Directory for JSON results",
    )
    parser.add_argument(
        "--config",
        "-c",
        default="balanced",
        choices=["balanced", "strict", "lite", "off"],
        help="Policy configuration preset",
    )
    parser.add_argument(
        "--save",
        "-s",
        action="store_true",
        help="Write results.json and summary markdown",
    )
    parser.add_argument(
        "--update-baseline",
        action="store_true",
        help="Copy saved results to benchmarks/baseline/results.json",
    )
    parser.add_argument(
        "--compare",
        help="Path to a previous results.json for version comparison",
    )
    parser.add_argument(
        "--regression-threshold",
        type=float,
        default=25.0,
        help="Max latency regression percent vs baseline (default 25)",
    )
    args = parser.parse_args()

    harness = BenchmarkHarness(output_dir=args.output_dir)
    config = PolicyConfig.from_mode(args.config)
    summary = harness.run_benchmark(config=config)

    print("\n=== PrivySHA Benchmark Summary ===")
    print(f"Version:        {harness.version_info.get('privysha_version', 'unknown')}")
    print(f"Tests run:      {summary.total_tests}")
    print(f"Passed:         {summary.passed_tests}")
    print(f"Failed:         {summary.failed_tests}")
    print(f"Avg latency:    {summary.avg_latency_ms:.1f} ms")
    print(f"Token reduction:{summary.avg_token_reduction_percentage:.1f}%")
    print(f"PII detected:   {summary.total_pii_detected}")
    print(f"False positive: {summary.false_positive_rate:.1%}")
    print(f"Fail-safe rate: {summary.fail_safe_rate:.1%}")
    print(f"P95 latency:    {summary.p95_latency_ms:.1f} ms")
    print(f"P99 latency:    {summary.p99_latency_ms:.1f} ms")

    if summary.false_positive_rate > 0.10:
        print("ERROR: false positive rate exceeds 10% threshold")
        return 1
    if summary.fail_safe_rate < 1.0:
        print("ERROR: fail-safe rate below 100%")
        return 1

    if args.save:
        results_file = harness.save_results()
        md_path = Path(args.output_dir) / "summary.md"
        md_path.parent.mkdir(parents=True, exist_ok=True)
        md_path.write_text(
            _render_summary(summary, harness.version_info, results_file),
            encoding="utf-8",
        )
        print(f"\nResults saved to: {results_file}")
        print(f"Summary saved to: {md_path}")
        if args.update_baseline:
            baseline_dir = ROOT / "benchmarks" / "baseline"
            baseline_dir.mkdir(parents=True, exist_ok=True)
            baseline_path = baseline_dir / "results.json"
            baseline_path.write_text(
                Path(results_file).read_text(encoding="utf-8"),
                encoding="utf-8",
            )
            (baseline_dir / "summary.md").write_text(
                md_path.read_text(encoding="utf-8"),
                encoding="utf-8",
            )
            print(f"Baseline updated: {baseline_path}")

    if args.compare:
        comparison = harness.check_regression(
            args.compare,
            latency_threshold_pct=args.regression_threshold,
        )
        print("\n=== Version Comparison ===")
        print(f"Current:  {comparison['current_version']}")
        print(f"Previous: {comparison['previous_version']}")
        if comparison.get("regressions"):
            print(f"Regressions: {', '.join(comparison['regressions'])}")
            return 1
        improved = comparison["overall_improvement"]
        print(f"Overall improvement: {'yes' if improved else 'no'}")

    return 0 if summary.failed_tests == 0 else 1


def _render_summary(summary, version_info: dict, results_file) -> str:
    ts = datetime.utcnow().strftime("%Y-%m-%d %H:%M UTC")
    results_name = Path(results_file).name
    return f"""# PrivySHA Benchmark Run

> Generated by `benchmarks/run_benchmarks.py` on {ts}

| Metric | Value |
|--------|-------|
| Version | {version_info.get('privysha_version', 'unknown')} |
| Tests | {summary.total_tests} ({summary.passed_tests} passed) |
| Avg latency | {summary.avg_latency_ms:.1f} ms |
| Avg token reduction | {summary.avg_token_reduction_percentage:.1f}% |
| PII detected | {summary.total_pii_detected} |
| False positive rate | {summary.false_positive_rate:.1%} |
| Fail-safe rate | {summary.fail_safe_rate:.1%} |
| P95 latency | {summary.p95_latency_ms:.1f} ms |
| P99 latency | {summary.p99_latency_ms:.1f} ms |

Full JSON: `{results_name}`

Re-run locally:

```bash
python benchmarks/run_benchmarks.py --save
```
"""


if __name__ == "__main__":
    raise SystemExit(main())
